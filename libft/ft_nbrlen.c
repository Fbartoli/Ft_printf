#include "libft.h"

int		ft_nbrlen(long n)
{
	if (n < 0)
		return (1 + ft_nbrlen(-n));
	else if (n / 10 > 0)
		return (1 + ft_nbrlen(n / 10));
	else
		return (1);
}
